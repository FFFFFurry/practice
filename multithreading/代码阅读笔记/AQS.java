    static final class Node {
        static final Node SHARED = new Node(); //共享节点标记
        static final Node EXCLUSIVE = null; //独占节点标记
        static final int CANCELLED =  1; //线程已经取消 表示节点由于超时或者中断
        static final int SIGNAL    = -1; //表示后继节点是阻塞状态 当当前节点释放资源或者被取消时 需要叫醒后继节点。
        static final int CONDITION = -2; //节点当前位于条件队列中
        static final int PROPAGATE = -3; // ？？？？？
        volatile int waitStatus; //节点状态
        volatile Node prev; //前驱节点
        volatile Node next; //后继节点
        volatile Thread thread; //当前线程
        Node nextWaiter;//????

        //是否为共享模式
        final boolean isShared() {
            return nextWaiter == SHARED;
        }

        //获取节点前驱
        final Node predecessor() throws NullPointerException {
            Node p = prev;
            if (p == null)
                throw new NullPointerException();
            else
                return p;
        }

        Node() {}

        Node(Thread thread, Node mode) {     // Used by addWaiter
            this.nextWaiter = mode;
            this.thread = thread;
        }

        Node(Thread thread, int waitStatus) { // Used by Condition
            this.waitStatus = waitStatus;
            this.thread = thread;
        }
    }


