    static final long spinForTimeoutThreshold = 1000L;

    //插入节点到队列中 必要的时候初始化队列
    private Node enq(final Node node) {
        for (;;) {
            Node t = tail;
            if (t == null) { // 队列为空时初始化队列
                if (compareAndSetHead(new Node()))
                    tail = head;
            } else {
                node.prev = t;
                if (compareAndSetTail(t, node)) {
                    t.next = node;
                    return t;
                }
            }
        }
    }

    //尝试快速加入队列 失败后采用自旋的方式加入节点
    private Node addWaiter(Node mode) {
        Node node = new Node(Thread.currentThread(), mode);
        // Try the fast path of enq; backup to full enq on failure
        Node pred = tail;
        if (pred != null) {
            node.prev = pred;
            if (compareAndSetTail(pred, node)) {
                pred.next = node;
                return node;
            }
        }
        enq(node);
        return node;
    }

    //设置头节点 通过设置null来促进GC
    private void setHead(Node node) {
        head = node;
        node.thread = null;
        node.prev = null;
    }

    //如果后继节点存在的话叫醒后继节点
    //释放后继接待你说明当前节点已经释放锁了，所以状态设为0 表示后继节点不用等待了，可以被唤醒了
    private void unparkSuccessor(Node node) {
        /*
         * If status is negative (i.e., possibly needing signal) try
         * to clear in anticipation of signalling.  It is OK if this
         * fails or if status is changed by waiting thread.
         */
        int ws = node.waitStatus;
        if (ws < 0)
            compareAndSetWaitStatus(node, ws, 0);

       //从尾部遍历找到没有被取消或者为null的后继节点，然后叫醒。
        Node s = node.next;
        if (s == null || s.waitStatus > 0) {
            s = null;
            //尾部遍历找到离当前节点最近的非取消节点，并且叫醒
            for (Node t = tail; t != null && t != node; t = t.prev)
                if (t.waitStatus <= 0)
                    s = t;
        }
        if (s != null)
            LockSupport.unpark(s.thread);
    }

    //释放共享锁
    private void doReleaseShared() {
        /*
        * todo 思考：tail和head是线程的共享变量，如果其他线程操作变化了 岂不是线程安全
        * 比如说一个当前队列是空队列，突然运行到 if (h == head)与 break 之间插入了节点，那doReleaseShared不是失败了
        * 没有释放线程，这是否是线程安全
        *
        * */

        //自旋
        for (;;) {
            Node h = head;
            //非空队列
            if (h != null && h != tail) {
                //释放锁时，如果状态为signal叫醒后继节点，并且将节点状态设置为0
                int ws = h.waitStatus;
                if (ws == Node.SIGNAL) {
                    if (!compareAndSetWaitStatus(h, Node.SIGNAL, 0))
                        continue;            // loop to recheck cases
                    unparkSuccessor(h);
                }
                //状态为0时，尝试将状态设置为PROPAGATE
                else if (ws == 0 &&
                        !compareAndSetWaitStatus(h, 0, Node.PROPAGATE))
                    continue;                // loop on failed CAS
            }
            //头节点变化的时候
            if (h == head)                   // loop if head changed
                break;
        }
    }

